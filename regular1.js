// g なし => 最初の1件が見つかったら検索終了
// g あり => 一致する文字列をすべて抽出

const text_1 = `
名前：太郎
電話：03-1234-5678
住所：北海道1-2-3
`;

const result_1 = text_1.match(/\d\d-\d\d\d\d-\d\d\d\d/g);
// => ["03-1234-5678"]
// \d => 半角数字1文字

const text_2 = `
名前：太郎
電話：03-1234-5678
電話：090-1234-5678
電話：0795-12-3456
電話：04992-1-2345
住所：東京1-2-3
`;
const result_2 = text_2.match(/\d{2,5}-\d{1,4}-\d{4}/g);
// => [ '03-1234-5678', '090-1234-5678', '0795-12-3456', '04992-1-2345' ]
// \d{x, y} => x文字以上y文字以下

const text_3 = `
名前：太郎
電話：03(1234)5678
電話：090-1234-5678
電話：0795(12)3456
電話：04992-1-2345
住所：東京1-2-3
`;

const result_3 = text_3.match(/\d{2,5}[-(]\d{1,4}[-)]\d{4}/g);
// => [ '03(1234)5678', '090-1234-5678', '0795(12)3456', '04992-1-2345' ]
// [AB] => AまたはBのいずれか1文字(文字数制限なし)

